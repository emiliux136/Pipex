#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*new;
	int		i;
	size_t	size;

	size = ft_strlen(src);
	i = 0;
	new = malloc(sizeof(char) * size + 1);
	if (!new)
		return (NULL);
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
